#include<stdio.h>
#include<math.h>
int main()
{
    printf("\nValue of 3 raised to the power 25=%f\n",pow(3,25));
    printf("\nValue of square root of 25 is=%f\n",sqrt(25));
    printf("\nValue of sine of 30 is=%f\n",sin(30));
    printf("\nValue of ceiling of 3.08754 is=%f\n",ceil(3.08754));
    printf("\nValue of floor of 4.5555 is=%f\n",floor(4.5555));
    printf("\nValue of natural logarithm of 100 is=%f\n",log(100));
    printf("\nValue of decimal logarithm of 100 is=%f\n",log10(100));
    printf("\nValue of e to the power 55 is=%f\n",exp(55));
    printf("\nAbsolute value of -25.5 is=%f\n",fabs(-25.5));
    printf("\nAbsolute value of -25 is=%d\n",abs(-25));

    return 0;
}
